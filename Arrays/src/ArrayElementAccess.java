public class ArrayElementAccess {
    public static void main(String[] args) {
        int[] intArrays = {10,20,30};
        int firstElement = intArrays[0];
        System.out.println(firstElement);
        int thirdElement = intArrays[2];
        System.out.println(thirdElement);
        int forthElement = intArrays[3];
        System.out.println(forthElement);
    }
}
